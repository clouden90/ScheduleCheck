#!/usr/bin/env python3
"""
driver for the schedule-conflict library
"""


from schedulecheck import meetinglib


class Testmeetinglib:

    def test_conflicts1(self):
        data = [['08:45', '09:00'], ['10:00', '11:00'], ['10:15', '10:30'],
                ['11:00', '13:00'], ['12:00', '13:00'], ['11:30', '11:45'],
                ['13:15', '14:00'], ['15:00', '15:30'], ['15:30', '16:00']]
        ref = [(['10:15', '10:30'], ['10:00', '11:00']),
               (['11:30', '11:45'], ['11:00', '13:00']),
               (['11:00', '13:00'], ['12:00', '13:00'])]
        assert ref == meetinglib.conflicts_check1(data)

    def test_conflicts2(self):
        data = [['08:45', '09:00'], ['10:00', '11:00'], ['10:15', '10:30'],
                ['11:00', '13:00'], ['12:00', '13:00'], ['11:30', '11:45'],
                ['13:15', '14:00'], ['15:00', '15:30'], ['15:30', '16:00']]
        ref = [(['10:15', '10:30'], ['10:00', '11:00']),
               (['12:00', '13:00'], ['11:00', '13:00']),
               (['11:30', '11:45'], ['11:00', '13:00'])]
        assert ref == meetinglib.conflicts_check2(data)
